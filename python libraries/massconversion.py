#massconversion.py
'''for the conversion of various mass'''

#functions
def kgtotonne(kg):
  '''convert mass kg to tonne'''
  return kg*0.001

def tonnetokg(tonne):
  '''convert mass tonne to kg'''
  return tonne/0.001

def kgtopound(kg):
  '''convert mass kg to pound'''
  return kg*2.20462

def poundtokg(pound):
  '''convert mass pound to kg'''
  return pound/0.001

#constants
kgtotonne=0.001
kgtopound=2.20462
